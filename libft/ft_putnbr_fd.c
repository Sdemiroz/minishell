#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*num;

	if (n == -2147483648)
	{
		num = "-2147483648";
		ft_putstr_fd(num, fd);
	}
	else if (n < 0)
	{
		n = -n;
		ft_putchar_fd('-', fd);
		ft_putnbr_fd(n, fd);
	}
	else if (n > 9)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
	else
		ft_putchar_fd(n + 48, fd);
}
